    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
       String descricao;
       int cod;
       double preco;
       int totalProdutos = 1000;
       
       descricao = this.Text_Desc.getText();
       cod = Integer.parseInt(this.Text_codigo.getText());
       preco = Double.parseDouble(this.Text_preco.getText());
       
        if(descricao.equals("")|| Text_codigo.equals("") || Text_preco.equals("")){
             JOptionPane.showMessageDialog(this, "Preencha todos os campos!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        else{
            Produtos px = new Produtos(cod, descricao, preco);
       lista.add(px);
       this.limparCampos();
        }
       
       
       
    }//GEN-LAST:event_jButton1MouseClicked

